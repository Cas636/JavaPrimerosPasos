package helloworld;


public class Variables {
    
     public static void main(String[] args){
    
        int telefono = 7895465;
        char letra = 'G';
        /* Variable de tipo Float
        Es necesario hacer un casteo y para hacerlo corto se pone f al final del dato
        */
        float peso = 65.2f;
        double ngrande = 76.484949884;
        System.out.println("El número de telefono es: "+telefono);
        System.out.println("La letra es: "+letra);
        System.out.println("El peso es: "+peso);
        System.out.println("El número grande es: "+ngrande);
    
    }
    
}
