package helloworld;


public class OperadoresRelacionales {
    
     public static void main(String[] args){
    
        int a = 10;
        int b = 5;
        // se realizan las operaciones relacionales con los datos declarados arriba
        System.out.println("a==b "+(a==b));
        System.out.println("a<b "+(a<b));
        System.out.println("a<=b "+(a<=b));
        System.out.println("a>b "+(a>b));
        System.out.println("a>=b "+(a>=b));
        System.out.println("a!=b "+(a!=b));
    }
    
}
